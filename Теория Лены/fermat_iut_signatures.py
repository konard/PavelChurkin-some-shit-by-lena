#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Обобщённое уравнение Ферма и подход IUT — вспомогательные расчёты.

    x^r + y^s = z^t   (x, y, z, r, s, t — натуральные, gcd(x,y,z)=1)

Скрипт без внешних зависимостей (только стандартная библиотека Python 3).
Все числовые утверждения отчёта `отчёт-<метка>.md` берутся из его вывода.

Что считает скрипт:
  1) проверяет 10 известных примитивных решений (т.н. решения Ферма–Каталана,
     при 1/r+1/s+1/t < 1);
  2) раскладывает сигнатуры (r,s,t) на три класса по знаку
     1/r + 1/s + 1/t − 1 (сферический / евклидов / гиперболический) — именно
     гиперболический класс даёт конечность числа решений (Дармон–Гранвиль);
  3) перебором на малом окне подтверждает «пустоту» при r,s,t >= 4 — это
     иллюстрация идеи «конечной проверки» (finite verification), на которую
     опираются IUT-оценки;
  4) считает связку с кубической моделью Теории Лены: случай r=s=t=3 — это и
     есть «барьер двух точек» (Великая теорема Ферма для куба).
"""

from math import gcd
from fractions import Fraction


# ---------------------------------------------------------------------------
# 1. Десять известных решений (1/r + 1/s + 1/t < 1).
#    Каждое решение записано как (подпись, [(база, показатель), ...], (z, t)).
# ---------------------------------------------------------------------------
KNOWN_SOLUTIONS = [
    ("1^p + 2^3 = 3^2  (Каталан)", (1, 9), (2, 3), (3, 2)),
    ("2^5 + 7^2 = 3^4",            (2, 5), (7, 2), (3, 4)),
    ("7^3 + 13^2 = 2^9",           (7, 3), (13, 2), (2, 9)),
    ("2^7 + 17^3 = 71^2",          (2, 7), (17, 3), (71, 2)),
    ("3^5 + 11^4 = 122^2",         (3, 5), (11, 4), (122, 2)),
    ("17^7 + 76271^3 = 21063928^2", (17, 7), (76271, 3), (21063928, 2)),
    ("1414^3 + 2213459^2 = 65^7",  (1414, 3), (2213459, 2), (65, 7)),
    ("9262^3 + 15312283^2 = 113^7", (9262, 3), (15312283, 2), (113, 7)),
    ("43^8 + 96222^3 = 30042907^2", (43, 8), (96222, 3), (30042907, 2)),
    ("33^8 + 1549034^2 = 15613^3", (33, 8), (1549034, 2), (15613, 3)),
]


def verify_known_solutions():
    """Проверяет арифметику 10 известных решений и их примитивность."""
    print("=== 1. Проверка 10 известных решений (Ферма–Каталан) ===")
    all_ok = True
    for name, (a, p), (b, q), (c, t) in KNOWN_SOLUTIONS:
        lhs = a ** p + b ** q
        rhs = c ** t
        ok = (lhs == rhs)
        coprime = gcd(gcd(a, b), c) == 1
        all_ok &= ok and coprime
        print(f"  [{'OK' if ok else 'BAD'}] {name:32s}  "
              f"{lhs} = {rhs}  (взаимно просты: {coprime})")
    print(f"  Итог: все 10 верны и примитивны: {all_ok}")
    # У всех десяти один из показателей равен 2 → ни одно не лежит в зоне
    # r,s,t >= 3 (Бил) и тем более r,s,t >= 4: это и объясняет, почему там
    # ожидается пустой список решений.
    has_two = all(2 in (p, q, t) for _, (_, p), (_, q), (_, t) in KNOWN_SOLUTIONS)
    print(f"  У каждого решения есть показатель 2 (значит, вне зоны Била "
          f"min>=3): {has_two}")
    print()
    return all_ok


# ---------------------------------------------------------------------------
# 2. Трихотомия сигнатур по 1/r + 1/s + 1/t.
# ---------------------------------------------------------------------------
def classify_signature(r, s, t):
    """'spherical' (>1), 'euclidean' (=1) или 'hyperbolic' (<1)."""
    val = Fraction(1, r) + Fraction(1, s) + Fraction(1, t)
    if val > 1:
        return "spherical"
    if val == 1:
        return "euclidean"
    return "hyperbolic"


def signature_trichotomy(max_exp=12):
    """Считает классы сигнатур (с точностью до перестановки) до max_exp."""
    print(f"=== 2. Трихотомия сигнатур (r,s,t) <= {max_exp}, "
          f"с точностью до перестановки ===")
    counts = {"spherical": 0, "euclidean": 0, "hyperbolic": 0}
    euclid = []
    spher = []
    for r in range(2, max_exp + 1):
        for s in range(r, max_exp + 1):
            for t in range(s, max_exp + 1):
                cls = classify_signature(r, s, t)
                counts[cls] += 1
                if cls == "euclidean":
                    euclid.append((r, s, t))
                elif cls == "spherical":
                    spher.append((r, s, t))
    print(f"  сферические (1/r+1/s+1/t>1, бесконечно решений): {counts['spherical']}  {spher}")
    print(f"  евклидовы   (=1, особый случай):                {counts['euclidean']}  {euclid}")
    print(f"  гиперболические (<1, КОНЕЧНО решений — Дармон–Гранвиль): {counts['hyperbolic']}")
    print("  Именно гиперболический класс изучают IUT и модулярный метод.")
    print()
    return counts


# ---------------------------------------------------------------------------
# 3. Иллюстрация «конечной проверки»: перебор на малом окне для r,s,t >= 4.
# ---------------------------------------------------------------------------
def integer_root(n, t):
    """Целый t-й корень из n, либо None, если n не является t-й степенью."""
    if n < 1:
        return None
    lo, hi = 1, 1
    while hi ** t < n:
        hi *= 2
    while lo <= hi:
        mid = (lo + hi) // 2
        p = mid ** t
        if p == n:
            return mid
        if p < n:
            lo = mid + 1
        else:
            hi = mid - 1
    return None


def search_no_small_solutions(base_bound=60, exps=(4, 5, 6)):
    """
    Перебирает примитивные x^r + y^s = z^t с показателями из exps (все >= 4)
    и базами до base_bound. Ожидаемый результат — НИ ОДНОГО решения:
    это локальная иллюстрация того, что в зоне r,s,t >= 4 список пуст, а
    IUT-оценка лишь делает такую проверку конечной для ВСЕХ масштабов.
    """
    print(f"=== 3. Перебор r,s,t из {exps} (все >= 4), базы x,y <= {base_bound} ===")
    found = []
    checked = 0
    for r in exps:
        for s in exps:
            for t in exps:
                for x in range(1, base_bound + 1):
                    xr = x ** r
                    for y in range(1, base_bound + 1):
                        if gcd(x, y) != 1:
                            continue
                        checked += 1
                        z = integer_root(xr + y ** s, t)
                        if z is not None and gcd(x, z) == 1 and gcd(y, z) == 1:
                            found.append((x, r, y, s, z, t))
    print(f"  проверено примитивных пар (x,y): ~{checked}")
    if found:
        print(f"  НАЙДЕНЫ решения (неожиданно!): {found}")
    else:
        print("  решений не найдено — согласуется с гипотезой (зона r,s,t>=4 пуста).")
    print()
    return found


# ---------------------------------------------------------------------------
# 4. Мост к Теории Лены: кубическая модель как частный случай.
# ---------------------------------------------------------------------------
def bridge_to_lena():
    print("=== 4. Связка с Теорией Лены (кубическая модель) ===")
    print("  Правило слияния Лены:  x_1^3 + ... + x_k^3 = z^3.")
    print("  Случай k=2  →  x^3 + y^3 = z^3  — сигнатура (3,3,3),")
    cls = classify_signature(3, 3, 3)
    val = Fraction(1, 3) * 3
    print(f"    1/3+1/3+1/3 = {val} → класс '{cls}'.")
    print("    Решений нет (Великая теорема Ферма для куба) — это и есть")
    print("    «барьер двух точек» Теории Лены: слияние двойкой невозможно.")
    print("  Обобщённое уравнение Ферма x^r+y^s=z^t расширяет это правило на")
    print("  разные показатели; IUT даёт конечную проверку для гиперболической зоны.")
    print()


if __name__ == "__main__":
    ok = verify_known_solutions()
    signature_trichotomy(max_exp=12)
    search_no_small_solutions(base_bound=60, exps=(4, 5, 6))
    bridge_to_lena()
    print("Готово. Все числа выше — прямой вывод этого скрипта.")
