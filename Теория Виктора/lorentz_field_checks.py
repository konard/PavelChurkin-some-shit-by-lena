"""
Проверка числовых утверждений к третьему отчёту по Теории Виктора —
о лоренц-инвариантности эфира, силе Кулона/Ампера, скин-эффекте,
эффекте Холла, эффекте Фарадея (постоянная Верде), зеемановском
расщеплении, сверхпроводимости (лондоновская глубина), постоянной
тонкой структуры «через протон/атом водорода», опыте Резерфорда,
дефекте массы и распаде свободного нейтрона.

Скрипт детерминирован, без внешних зависимостей (только стандартная
библиотека Python 3). Все числа, помеченные в отчёте как «(скрипт §N)», —
прямой вывод этого файла.

Запуск:
    python3 "Теория Виктора/lorentz_field_checks.py"
"""

import math

# --- Фундаментальные константы (значения системы СИ / CODATA) ---
EPS0 = 8.8541878128e-12      # Ф/м   — электрическая постоянная (удельная ёмкость эфира)
MU0 = 1.25663706212e-6       # Гн/м  — магнитная постоянная (удельная индуктивность эфира)
C_DEF = 299792458.0          # м/с   — скорость света (определена точно в СИ)
H = 6.62607015e-34           # Дж·с  — постоянная Планка
HBAR = H / (2 * math.pi)
E_CHARGE = 1.602176634e-19   # Кл    — элементарный заряд
M_E = 9.1093837015e-31       # кг    — масса электрона
M_P = 1.67262192369e-27      # кг    — масса протона
M_N = 1.67492749804e-27      # кг    — масса нейтрона
MU_B = 9.2740100783e-24      # Дж/Тл — магнетон Бора
ALPHA_CODATA = 7.2973525693e-3   # постоянная тонкой структуры (табл.)
EV = E_CHARGE                # 1 эВ в джоулях
MEV = 1e6 * EV               # 1 МэВ в джоулях

K_E = 1.0 / (4 * math.pi * EPS0)  # постоянная Кулона, Н·м^2/Кл^2


def section(title):
    print("\n" + "=" * 70)
    print(title)
    print("=" * 70)


def check_lorentz_and_neutrino():
    """§1. Лоренц-инвариантность среды и «телепортация против нейтрино».

    (а) Эфир как «канат с выделенной системой покоя» предсказывал бы
        анизотропию скорости света (эфирный ветер). Современные резонаторные
        опыты ограничивают анизотропию на уровне Δc/c < 1e-17 — выделенной
        системы покоя нет, отсюда обязательное требование лоренц-инвариантности.
    (б) Нейтрино проходят сквозь Землю НЕ телепортацией, а из-за крошечного
        сечения. Оценим вероятность взаимодействия на пути сквозь Землю:
            P ≈ n · σ · L,
        n — концентрация нуклонов, σ — сечение (МэВ-нейтрино ~1e-47 м^2),
        L — диаметр Земли. Получается ~4e-10: проходят почти все, но это
        вероятностное прохождение, а не мгновенный «перескок».
    """
    section("1. Лоренц-инвариантность (эфирный ветер) и нейтрино сквозь Землю")
    print("  (а) граница анизотропии скорости света (резонаторы): Δc/c < 1e-17")
    print("      => выделенной системы покоя эфира нет; нужна лоренц-инвариантность.")
    # (б) прохождение нейтрино сквозь Землю
    rho_earth = 5515.0          # кг/м^3 — средняя плотность Земли
    diam_earth = 1.2742e7       # м      — диаметр Земли
    sigma_nu = 1e-47            # м^2    — порядок сечения нейтрино ~МэВ на нуклоне
    n_nucleon = rho_earth / M_P  # ~нуклонов на м^3 (m_p ≈ масса нуклона)
    column = n_nucleon * diam_earth
    p_interact = column * sigma_nu
    print(f"  (б) концентрация нуклонов n = {n_nucleon:.3e} 1/м^3")
    print(f"      колонная плотность n·L  = {column:.3e} 1/м^2")
    print(f"      P(взаимодействие)≈n·σ·L = {p_interact:.3e}  (σ≈1e-47 м^2)")
    print("      => сквозь Землю проходит ~(1 − 4e-10) нейтрино: мала σ, а не")
    print("         «телепортация». Прохождение вероятностное, не мгновенный скачок.")


def check_coulomb_constant():
    """§2. «Сила Кулона вложена руками»: 1/(4π ε0) — это масштаб, не вывод.

    Из (ε0, μ0) выводятся c и Z0, но абсолютная величина силы между зарядами
    задаётся постоянной Кулона k_e = 1/(4π ε0) — её значение в модель
    *вкладывается* выбором ε0, а не выводится из «механики поляризации».
    """
    section("2. Постоянная Кулона k_e = 1/(4π ε0) — масштаб силы, заданный ε0")
    z0 = math.sqrt(MU0 / EPS0)
    print(f"  k_e = 1/(4π ε0) = {K_E:.6e} Н·м^2/Кл^2")
    print(f"  c   = 1/sqrt(ε0 μ0) = {1/math.sqrt(EPS0*MU0):.9e} м/с (= c точно)")
    print(f"  Z0  = sqrt(μ0/ε0)   = {z0:.4f} Ом")
    print("  => c и Z0 СЛЕДУЮТ из (ε0, μ0); но численное значение силы Кулона")
    print("     держится на самом ε0 — оно вложено руками, не выведено из механики.")


def check_skin_effect_and_reactance():
    """§3. Скин-эффект и индуктивное сопротивление: рост с частотой.

    Глубина скин-слоя в проводнике:  δ = sqrt(2/(ω μ0 σ)),  ω = 2π f.
    Индуктивное сопротивление:        X_L = ω L = 2π f L.
    Оба эффекта — прямое следствие конечной «скорости перекладывания
    поляризации»: чем быстрее меняется ток, тем тоньше слой и тем больше
    «инерционное» сопротивление.
    """
    section("3. Скин-эффект δ(f) и индуктивное сопротивление X_L=2πfL (медь)")
    sigma_cu = 5.96e7   # См/м — проводимость меди
    print("  Глубина скин-слоя δ = sqrt(2/(ω μ0 σ)) для меди (σ=5.96e7 См/м):")
    for f in (50.0, 1e3, 1e6, 1e9):
        w = 2 * math.pi * f
        delta = math.sqrt(2.0 / (w * MU0 * sigma_cu))
        print(f"    f = {f:>10.0f} Гц : δ = {delta*1e3:10.4f} мм")
    print("  Индуктивное сопротивление X_L = 2π f L для L = 1 мкГн:")
    L = 1e-6
    for f in (50.0, 1e3, 1e6, 1e9):
        print(f"    f = {f:>10.0f} Гц : X_L = {2*math.pi*f*L:10.4e} Ом")
    print("  => с ростом частоты ток вытесняется к поверхности (δ↓), а")
    print("     'инерция поляризации' растёт линейно по f (X_L↑).")


def check_ampere_force():
    """§4. Сила Ампера между двумя параллельными токами — даёт число.

    F/L = μ0 I1 I2 / (2π d).  Для I1=I2=1 А, d=1 м: ровно 2e-7 Н/м —
    это и есть историческое определение ампера. Сонаправленные токи
    притягиваются, встречные — отталкиваются.
    """
    section("4. Сила Ампера F/L = μ0 I1 I2 /(2π d) (старое определение ампера)")
    for I, d in ((1.0, 1.0), (10.0, 0.01)):
        f_per_l = MU0 * I * I / (2 * math.pi * d)
        print(f"  I={I:>5.1f} А, d={d:>5.2f} м : F/L = {f_per_l:.4e} Н/м")
    print("  => 1 А, 1 м ⇒ 2e-7 Н/м (исходное определение ампера до 2019 г.).")
    print("     Сонаправленные токи притягиваются (область между ними — пониженной")
    print("     плотности энергии поля), встречные — отталкиваются.")


def check_hall_sign():
    """§5. Эффект Холла: знак коэффициента = знак носителей.

    R_H = 1/(n q).  Для электронов q=−e ⇒ R_H<0; для дырок q=+e ⇒ R_H>0.
    Смена знака U_H — «знак деформации эфира» в образе Виктора.
    """
    section("5. Коэффициент Холла R_H = 1/(n q): знак = тип носителя")
    n_cu = 8.49e28   # 1/м^3 — концентрация электронов проводимости в меди
    R_H_electron = 1.0 / (n_cu * (-E_CHARGE))
    print(f"  медь (электроны), n={n_cu:.3e} 1/м^3 : R_H = {R_H_electron:.3e} м^3/Кл (<0)")
    print(f"  дырочный проводник (q=+e), та же n     : R_H = {-R_H_electron:.3e} м^3/Кл (>0)")
    print("  => знак холловского напряжения переключается со знаком носителей —")
    print("     в образе модели это смена знака 'деформации эфира' (+/−).")


def check_faraday_and_zeeman():
    """§6. Эффект Фарадея (Верде) и зеемановское расщепление.

    Магнитное вращение плоскости поляризации: β = V·B·d.
    Классическая оценка Лармора даёт характерную частоту прецессии
        ν_L = eB/(4π m_e) = 14,0 ГГц/Тл  (нормальный Зееман / половина циклотрона),
    а зеемановский сдвиг энергии ΔE = μ_B·B.
    Постоянная Верде воды ~3,8 рад/(Тл·м) при 589 нм — её знак и величина
    задаются той же ларморовской прецессией связанных электронов.
    """
    section("6. Эффект Фарадея (Верде) и зеемановское расщепление ΔE=μ_B·B")
    nu_larmor = E_CHARGE / (4 * math.pi * M_E)   # Гц/Тл
    print(f"  ларморовская частота ν_L = eB/(4π m_e) = {nu_larmor:.4e} Гц/Тл")
    print(f"                            = {nu_larmor/1e9:.2f} ГГц/Тл (нормальный Зееман)")
    for B in (1.0, 5.0):
        dE = MU_B * B
        print(f"  B={B:>4.1f} Тл : ΔE=μ_B·B = {dE:.4e} Дж = {dE/EV*1e6:.3f} мкэВ"
              f"  (ν={dE/H/1e9:.2f} ГГц)")
    V_water = 3.8  # рад/(Тл·м) при 589 нм — табличный порядок
    for B, d in ((1.0, 0.1),):
        beta = V_water * B * d
        print(f"  вода (V≈3,8 рад/(Тл·м)): β=V·B·d при B={B} Тл, d={d} м = {beta:.3f} рад")
    print("  => вращение и расщепление управляются одной ларморовской прецессией;")
    print("     её знак и есть 'знак закрутки эфира', величины — табличные.")


def check_london_depth():
    """§7. Сверхпроводимость: лондоновская глубина проникновения.

    λ_L = sqrt( m_e / (μ0 n_s e^2) ).  Поле спадает в сверхпроводнике на
    глубине ~десятков нм (эффект Мейснера). Это масштаб «выталкивания поля».
    """
    section("7. Лондоновская глубина проникновения λ_L (эффект Мейснера)")
    for n_s in (1e28, 4e28):
        lam = math.sqrt(M_E / (MU0 * n_s * E_CHARGE**2))
        print(f"  n_s={n_s:.0e} 1/м^3 : λ_L = {lam*1e9:.2f} нм")
    print("  => поле выталкивается, проникая лишь на десятки нм — масштаб")
    print("     'отказа эфира держать вихревое состояние' в образе модели.")


def check_alpha_via_proton():
    """§8. Постоянная тонкой структуры «через протон/атом водорода».

    α безразмерна и НЕ зависит от массы пробной частицы:
        α = r_cl / ƛ_C  для ЛЮБОЙ заряженной частицы (масса сокращается),
        где r_cl = e^2/(4π ε0 m c^2),  ƛ_C = ħ/(m c).
    Поэтому «посчитать α через размер протона» даёт ровно то же 1/137, что и
    через электрон. Боровский радиус: a0 = ƛ_C(e)/α. Это показывает, что α —
    единственный недостающий безразмерный кирпич, и его не «починить» заменой
    электрона на протон или атом водорода.
    """
    section("8. α 'через протон/водород': одно и то же 1/137 (масса сокращается)")
    for name, m in (("электрон", M_E), ("протон", M_P)):
        r_cl = E_CHARGE**2 / (4 * math.pi * EPS0 * m * C_DEF**2)
        lam_C = HBAR / (m * C_DEF)
        alpha = r_cl / lam_C
        print(f"  {name:8s}: r_cl={r_cl:.4e} м, ƛ_C={lam_C:.4e} м, r_cl/ƛ_C={alpha:.6e}")
    a0 = HBAR / (M_E * C_DEF * ALPHA_CODATA)
    print(f"  боровский радиус a0 = ƛ_C(e)/α = {a0:.6e} м,  1/α = {1/ALPHA_CODATA:.4f}")
    print("  => α от массы пробника НЕ зависит: 'через протон' выходит то же 1/137.")
    print("     Замена электрона на протон/водород проблему α не снимает.")


def check_rutherford():
    """§9. Опыт Резерфорда: расстояние наибольшего сближения и размер ядра.

    Для лобового удара α-частицы (заряд 2e, энергия E) о ядро (заряд Z e)
    кулоновский барьер останавливает её на
        d = k_e · (2e)(Z e) / E = 2 Z e^2 k_e / E.
    Рассеяние на БОЛЬШИЕ углы возможно только если весь заряд Z e сосредоточен
    в области < d. Для золота (Z=79) при E≈7,7 МэВ получаем d~3e-14 м — отсюда
    верхняя граница на размер ядра. Для алюминия (Z=13) d меньше: при достаточной
    энергии α-частица достаёт ядро, и появляются отклонения от формулы
    Резерфорда — так измеряют сам размер ядра.
    """
    section("9. Резерфорд: d = 2 Z e^2 k_e / E (верхняя граница размера ядра)")
    for name, Z, E_mev in (("золото", 79, 7.7), ("алюминий", 13, 7.7)):
        E = E_mev * MEV
        d = 2 * Z * E_CHARGE**2 * K_E / E
        print(f"  {name:9s} (Z={Z:2d}), E={E_mev} МэВ : d = {d:.3e} м = {d*1e15:.1f} фм")
    print("  => для золота 'ядро' < ~30 фм; рассеяние назад невозможно без")
    print("     сосредоточенного заряда — атом-в-целом такого угла не даёт.")
    print("     Для лёгкого алюминия α достаёт ближе ⇒ отклонения от Резерфорда")
    print("     прямо измеряют размер ядра (он не равен размеру атома ~1e-10 м).")


def check_mass_defect_and_neutron():
    """§10. Дефект массы и распад свободного нейтрона.

    Дейтрон: энергия связи B = (m_p + m_n − m_d) c^2 ≈ 2,224 МэВ.
    Свободный нейтрон: Q = (m_n − m_p − m_e) c^2 ≈ 0,782 МэВ > 0 ⇒ нейтрон
    нестабилен (β-распад, время жизни ~880 с). Это твёрдые числа ядерной
    физики, которых у «упругого эфира» (ε0, μ0) нет.
    """
    section("10. Дефект массы (дейтрон) и Q-значение распада нейтрона")
    m_d = 3.3435837724e-27  # кг — масса дейтрона (CODATA)
    B = (M_P + M_N - m_d) * C_DEF**2
    print(f"  дейтрон: B=(m_p+m_n−m_d)c^2 = {B/MEV:.3f} МэВ  (энергия связи)")
    Q = (M_N - M_P - M_E) * C_DEF**2
    print(f"  нейтрон: Q=(m_n−m_p−m_e)c^2 = {Q/MEV:.3f} МэВ (>0 ⇒ β-распад)")
    print("  => свободный нейтрон распадается (τ≈880 с); связанный в ядре — может")
    print("     быть стабилен. 'Нуклоны как капли' не отменяют этих чисел: они")
    print("     из ядерной/слабой физики, вне (ε0, μ0).")


def main():
    print("Проверка чисел к 3-му отчёту Теории Виктора")
    print("(лоренц-инвариантность, Кулон/Ампер, скин-эффект, Холл, Фарадей/Зееман,")
    print(" сверхпроводимость, α через протон, Резерфорд, дефект массы)")
    check_lorentz_and_neutrino()
    check_coulomb_constant()
    check_skin_effect_and_reactance()
    check_ampere_force()
    check_hall_sign()
    check_faraday_and_zeeman()
    check_london_depth()
    check_alpha_via_proton()
    check_rutherford()
    check_mass_defect_and_neutron()
    print("\nГотово. Все числа выше воспроизводимы запуском этого скрипта.")


if __name__ == "__main__":
    main()
