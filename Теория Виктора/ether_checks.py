#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Проверка числовых утверждений к отчёту по Теории Виктора
(эфир как непрерывный диэлектрик с удельной ёмкостью ε0 и удельной
индуктивностью μ0).

Скрипт детерминирован, без внешних зависимостей (только стандартная
библиотека Python 3). Все «механически проверено» числа в отчёте — прямой
вывод этого файла.

Запуск:
    python3 "Теория Виктора/ether_checks.py"
"""

import math

# --- Фундаментальные константы (значения системы СИ / CODATA) ---
EPS0 = 8.8541878128e-12   # Ф/м   — электрическая постоянная (удельная ёмкость эфира)
MU0 = 1.25663706212e-6    # Гн/м  — магнитная постоянная (удельная индуктивность эфира)
C_DEF = 299792458.0       # м/с   — скорость света (определена точно в СИ)
H = 6.62607015e-34        # Дж·с  — постоянная Планка
HBAR = H / (2 * math.pi)
E_CHARGE = 1.602176634e-19  # Кл   — элементарный заряд
M_E = 9.1093837015e-31    # кг    — масса электрона


def section(title):
    print("\n" + "=" * 70)
    print(title)
    print("=" * 70)


def check_speed_of_light():
    """C = 1/sqrt(ε0·μ0): скорость ЭМ-волны в эфире как в среде с ε0, μ0."""
    section("1. Скорость света как скорость волны в эфире:  c = 1/sqrt(ε0·μ0)")
    c = 1.0 / math.sqrt(EPS0 * MU0)
    rel = abs(c - C_DEF) / C_DEF
    print(f"  1/sqrt(ε0·μ0) = {c:.6f} м/с")
    print(f"  c (по определению СИ) = {C_DEF:.6f} м/с")
    print(f"  относительное расхождение = {rel:.2e}  (ограничено точностью ε0, μ0)")
    return c


def check_vacuum_impedance():
    """Характеристический импеданс эфира Z0 = sqrt(μ0/ε0).

    Прямое следствие модели Виктора: если эфир — распределённая среда с
    погонной ёмкостью ε0 и погонной индуктивностью μ0, то это в точности
    длинная линия. У длинной линии скорость волны v = 1/sqrt(L·C), а
    волновое сопротивление Z = sqrt(L/C). Подставляя L→μ0, C→ε0, получаем
    c = 1/sqrt(ε0·μ0) и Z0 = sqrt(μ0/ε0) ≈ 376.73 Ом — табличный «импеданс
    вакуума». Совпадение электротехнической формулы линии передачи с
    электродинамикой вакуума — сильный аргумент в пользу интуиции Виктора.
    """
    section("2. Эфир как длинная линия:  Z0 = sqrt(μ0/ε0)  (волновое сопротивление)")
    z0 = math.sqrt(MU0 / EPS0)
    z0_alt = MU0 * C_DEF  # эквивалентная форма Z0 = μ0·c
    print(f"  sqrt(μ0/ε0) = {z0:.6f} Ом")
    print(f"  μ0·c        = {z0_alt:.6f} Ом   (та же величина, иной вид)")
    print(f"  табличный импеданс вакуума ≈ 376.730 Ом")
    print("  => среда (ε0, μ0) ведёт себя как линия передачи: v=1/sqrt(LC), Z=sqrt(L/C)")
    return z0


def check_photoeffect_red_border():
    """Красная граница фотоэффекта: λ_max = h·c/W (порог по работе выхода)."""
    section("3. Красная граница фотоэффекта:  λ_max = h·c / W")
    # Работа выхода нескольких металлов, эВ -> Дж
    metals = {"Cs (цезий)": 2.14, "Na (натрий)": 2.28, "Zn (цинк)": 4.33, "Pt (платина)": 5.65}
    for name, w_ev in metals.items():
        w = w_ev * E_CHARGE
        lam = H * C_DEF / w
        print(f"  {name:14s} W={w_ev:>4.2f} эВ -> λ_max = {lam*1e9:7.1f} нм")
    print("  Порог по ЧАСТОТЕ (энергии кванта), а не по интенсивности — ключевой факт.")


def check_de_broglie():
    """Длина волны де Бройля электрона и условие дифракции."""
    section("4. Волна де Бройля электрона:  λ = h / p")
    for u_volt in (54.0, 100.0, 10000.0):  # 54 В — опыт Дэвиссона–Джермера
        # нерелятивистски: eU = p^2/(2m) -> p = sqrt(2 m e U)
        p = math.sqrt(2 * M_E * E_CHARGE * u_volt)
        lam = H / p
        print(f"  U={u_volt:>8.1f} В -> p={p:.3e} кг·м/с -> λ={lam*1e12:7.2f} пм")
    print("  При 54 В λ≈167 пм — порядка межатомных расстояний в Ni: видна дифракция.")


def check_lc_resonance_analogy():
    """Иллюстрация: время поляризации участка эфира ~ 1/c на единицу длины."""
    section("5. «Инерция поляризации»: задержка на метр пути возмущения")
    c = 1.0 / math.sqrt(EPS0 * MU0)
    t_per_m = 1.0 / c
    print(f"  Возмущение поляризации проходит 1 м за {t_per_m:.3e} с = 1/c.")
    print("  Конечность c == конечная скорость 'перекладывания' поляризации эфира:")
    print("  магнетизм (μ0) — мера сопротивления среды изменению этого состояния.")


def main():
    print("Проверка числовых утверждений к Теории Виктора (эфир = среда с ε0, μ0)")
    check_speed_of_light()
    check_vacuum_impedance()
    check_photoeffect_red_border()
    check_de_broglie()
    check_lc_resonance_analogy()
    print("\nГотово. Все числа выше воспроизводимы запуском этого скрипта.")


if __name__ == "__main__":
    main()
