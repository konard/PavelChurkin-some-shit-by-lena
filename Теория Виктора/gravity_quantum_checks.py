#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Проверка числовых утверждений ко второму отчёту по Теории Виктора —
о гравитации, о попытке вывести квант из размера электрона и упругости
эфира, и об атоме как «безъядерной области поверхностного натяжения эфира».

Скрипт детерминирован, без внешних зависимостей (только стандартная
библиотека Python 3). Все числа, помеченные в отчёте как «(скрипт §N)», —
прямой вывод этого файла.

Запуск:
    python3 "Теория Виктора/gravity_quantum_checks.py"
"""

import math

# --- Фундаментальные константы (значения системы СИ / CODATA) ---
EPS0 = 8.8541878128e-12     # Ф/м   — электрическая постоянная (удельная ёмкость эфира)
MU0 = 1.25663706212e-6      # Гн/м  — магнитная постоянная (удельная индуктивность эфира)
C_DEF = 299792458.0         # м/с   — скорость света (определена точно в СИ)
H = 6.62607015e-34          # Дж·с  — постоянная Планка
HBAR = H / (2 * math.pi)
E_CHARGE = 1.602176634e-19  # Кл    — элементарный заряд
M_E = 9.1093837015e-31      # кг    — масса электрона
M_P = 1.67262192369e-27     # кг    — масса протона
G_NEWTON = 6.67430e-11      # м^3/(кг·с^2) — гравитационная постоянная
ALPHA_CODATA = 7.2973525693e-3  # постоянная тонкой структуры (табл.)

# Астрономические параметры для оценки «эфирного градиента» (гравитация)
M_EARTH = 5.972e24          # кг
R_EARTH = 6.371e6           # м
M_SUN = 1.98892e30          # кг
R_SUN = 6.957e8             # м


def section(title):
    print("\n" + "=" * 70)
    print(title)
    print("=" * 70)


def check_gravity_vs_electric():
    """Во сколько раз гравитация слабее электростатики между двумя частицами.

    Отношение не зависит от расстояния (оба закона ~ 1/r^2):
        F_grav / F_el = G m^2 / ( q^2 / (4π ε0) ).
    Для двух электронов выходит ~2.4e-43, для двух протонов ~8.1e-37.
    Это и есть количественный смысл фразы «гравитация — суммарный феномен
    скопления атомов»: на уровне одной частицы она ничтожна, заметной её
    делает только сложение огромного числа масс.
    """
    section("1. Гравитация против электростатики: F_grav / F_el (не зависит от r)")
    ke = 1.0 / (4 * math.pi * EPS0)  # постоянная Кулона
    for name, m in (("электрон", M_E), ("протон", M_P)):
        ratio = (G_NEWTON * m * m) / (ke * E_CHARGE * E_CHARGE)
        print(f"  два {name:8s}: F_grav/F_el = {ratio:.3e}")
    print("  => между элементарными частицами гравитация на 36–43 порядка слабее.")
    print("     Заряды у вещества скомпенсированы, и на больших масштабах")
    print("     остаётся только нескомпенсированная, всегда притягивающая масса.")


def check_alpha_and_length_ladder():
    """Лестница масштабов электрона держится на ОДНОМ числе — α.

        r_e   = e^2 / (4π ε0 m_e c^2)        — классический радиус (без h!)
        ƛ_C   = ħ / (m_e c)                  — приведённая комптоновская длина
        a0    = ħ / (m_e c α)                — боровский радиус (размер атома)
    Связи:  r_e = α·ƛ_C = α^2·a0,   ƛ_C = α·a0.
    Классический радиус r_e строится БЕЗ постоянной Планка, а размер атома a0 —
    нет: между ними множитель 1/α^2 ≈ 1.9e4. Значит «размер электрона» сам по
    себе НЕ задаёт атомный масштаб — для него обязателен независимый квант
    действия ħ. Это ключевой количественный довод против вывода кванта только
    из размера электрона и упругости эфира.
    """
    section("2. Лестница масштабов электрона и постоянная тонкой структуры α")
    r_e = E_CHARGE**2 / (4 * math.pi * EPS0 * M_E * C_DEF**2)
    lam_C_bar = HBAR / (M_E * C_DEF)
    a0 = HBAR / (M_E * C_DEF * ALPHA_CODATA)
    # α, восстановленная из определения — проверка самосогласованности
    alpha_calc = E_CHARGE**2 / (4 * math.pi * EPS0 * HBAR * C_DEF)
    print(f"  классический радиус r_e = {r_e:.4e} м  (построен БЕЗ h)")
    print(f"  компт. длина (привед.) ƛ_C = {lam_C_bar:.4e} м")
    print(f"  боровский радиус   a0 = {a0:.4e} м  (требует h)")
    print(f"  α (из e^2/4πε0ħc)   = {alpha_calc:.8e}")
    print(f"  α (табличная)       = {ALPHA_CODATA:.8e}")
    print(f"  проверка цепочки: r_e/ƛ_C = {r_e/lam_C_bar:.6e}  ~ α = {ALPHA_CODATA:.6e}")
    print(f"  проверка цепочки: ƛ_C/a0  = {lam_C_bar/a0:.6e}  ~ α = {ALPHA_CODATA:.6e}")
    print(f"  a0/r_e = {a0/r_e:.4e}  ~ 1/α^2 = {1/ALPHA_CODATA**2:.4e}")
    print("  => атом больше 'размера электрона' в 1/α^2 ≈ 1.9e4 раз: масштаб")
    print("     атома задаёт h, а не сам по себе размер электрона.")


def check_h_dimensional_gap():
    """Можно ли собрать h из (ε0, μ0, e, m_e)? Размерный разбор.

    Из ε0, μ0, e, m_e БЕЗ кванта действия строится только одна
    'естественная' энергия и один масштаб длины (классический радиус r_e и
    энергия m_e c^2). Действие на этих масштабах:
        S_cl = m_e c^2 · (r_e / c) = m_e c · r_e.
    Сравним его с ħ. Отношение оказывается равно α — то есть, чтобы из
    'размера электрона' получить ħ, нужно УЖЕ знать α (1/137), а α в чисто
    электрическую модель (ε0, μ0) не входит. Это и есть недостающий кирпич.
    """
    section("3. Размерный зазор: действие 'размера электрона' против ħ")
    r_e = E_CHARGE**2 / (4 * math.pi * EPS0 * M_E * C_DEF**2)
    S_cl = M_E * C_DEF * r_e          # естественное действие классич. масштаба
    print(f"  естественное действие S_cl = m_e·c·r_e = {S_cl:.4e} Дж·с")
    print(f"  ħ                                       = {HBAR:.4e} Дж·с")
    print(f"  S_cl / ħ = {S_cl/HBAR:.6e}  ~ α = {ALPHA_CODATA:.6e}")
    print("  => h НЕ выводится из (ε0, μ0, e, m_e): между 'классическим действием'")
    print("     и ħ стоит множитель α, не содержащийся в чисто электрич. эфире.")


def check_ether_gradient_gravity():
    """Гравитация как 'эфирный градиент' (модель поляризуемого вакуума, PV).

    Если трактовать тяготение как изменение свойств эфира (ε0, μ0 → c),
    то в слабом поле эффективный показатель преломления вакуума
        n ≈ 1 + 2Φ/c^2,   Φ = GM/r  (ньютоновский потенциал, по модулю).
    Безразмерная 'глубина' 2Φ/c^2 = 2GM/(r c^2) — она же удвоенный
    гравитационный сдвиг частоты. Числа крошечны: у поверхности Земли ~1e-9,
    Солнца ~4e-6 — поэтому 'неизотропность эфира' ничтожна и в быту незаметна,
    но именно она в этой картине отвечает за ход часов и отклонение света.
    """
    section("4. 'Эфирный градиент' гравитации: 2GM/(r c^2) у поверхности тел")
    for name, m, r in (("Земля", M_EARTH, R_EARTH), ("Солнце", M_SUN, R_SUN)):
        phi = G_NEWTON * m / r
        depth = 2 * phi / C_DEF**2
        print(f"  {name:7s}: Φ=GM/R={phi:.4e} м^2/с^2,  2Φ/c^2 = {depth:.4e}")
    print("  => 'плотность эфира' меняется на 1e-9 (Земля) … 4e-6 (Солнце).")
    print("     Это язык модели поляризуемого вакуума (Дикке, Путхофф); ОТО даёт")
    print("     те же величины в слабом поле, но как геометрию пространства-времени.")


def main():
    print("Проверка чисел ко 2-му отчёту Теории Виктора (гравитация, квант, атом)")
    check_gravity_vs_electric()
    check_alpha_and_length_ladder()
    check_h_dimensional_gap()
    check_ether_gradient_gravity()
    print("\nГотово. Все числа выше воспроизводимы запуском этого скрипта.")


if __name__ == "__main__":
    main()
